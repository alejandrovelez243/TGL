# SUMA

# TIPOS
# int para entero
# float para numeros con decimales
# str para cadenas de texto
# bool para datos true o false
# list para arreglos
# dict para dictionarios o jsons

def suma(a: int, b: int):
    return a + b

print(suma(10, 20))


def resta(a: int, b: int):
    return a - b

print(resta(4, 10))

def multiplicacion(a: int, b: int):
    return a * b

print(multiplicacion(10, 2))

def division(a: int, b: int):
    return int(a / b)


print(division(20, 2))

def modulo(a: int, b: int):
    return a % b

print(modulo(10, 4))

def potencia(a: int, b: int):
    return a ** b

print(potencia(2, 3))

